<h2>
  Moving around in GPlace
</h2>
<div class="m-4">
  <ul class="list-disc">
    <li>
      You can move around in GPlace using the <code>arrow keys</code>.
    </li>
    <li>
      You can also use the <code>z</code> key to zoom in and out.
    </li>
    <li>
      You can use the <code>e</code> key to open the eye dropper.
    </li>
    <li>
      You can use the <code>Enter</code> key to place a pixel.
    </li>
  </ul>
</div>  
